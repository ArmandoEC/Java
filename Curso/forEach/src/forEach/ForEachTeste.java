package forEach;

public class ForEachTeste {

	public static void main(String[] args) {
		
		String [] vet = new String [] {"joao","maria","carlos"}; //declarando um vetor e inicializando ele
		
		
		for(String obj : vet) { //for each com tipo de dado e o apelido , que percorre todo o array vet
			System.out.println(obj); //imprimindo apartir do apelido
		}
		
		

	}

}
